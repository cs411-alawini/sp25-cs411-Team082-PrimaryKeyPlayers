    AST DECIMAL(4,1) NOT NULL,
    STL DECIMAL(4,1) NOT NULL,
    BLK DECIMAL(4,1) NOT NULL,
    TOV DECIMAL(4,1) NOT NULL,
    PF DECIMAL(4,1) NOT NULL,
    PTS DECIMAL(4,1) NOT NULL,
